#ifndef GAMEPLAY_H
#define GAMEPLAY_H

/// \author Michael Woods
/// \version 1.0
/// \date 16/5/2017
/// \class Gameplay
/// \brief This class controls variables for the core gameplay and initialises them.

class gamePlay
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief constructor for class
    //----------------------------------------------------------------------------------------------------------------------
    gamePlay();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets cureStage
    //----------------------------------------------------------------------------------------------------------------------
    int cureStage() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets Ingredients value
    //----------------------------------------------------------------------------------------------------------------------
    int ingredients() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief get heartcount value
    //----------------------------------------------------------------------------------------------------------------------
    int heartcount() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets cureStage when called
    /// @param[in] passes through cureStage variable
    //----------------------------------------------------------------------------------------------------------------------
    void setCureStage(int cureStage);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets Ingredients value
    /// @param[in] passes through ingredients value
    //----------------------------------------------------------------------------------------------------------------------
    void setIngredients(int _ingredients);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief set heartcount value
    /// @param[in] pass through heartcount variable
    //----------------------------------------------------------------------------------------------------------------------
    void setHeartcount(int _heartcount);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets flag for lavender bool
    //----------------------------------------------------------------------------------------------------------------------
    bool lavenderFlag() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief set lavender bool flag
    /// @param[in] pass through lavender flag
    //----------------------------------------------------------------------------------------------------------------------
    void setLavenderFlag(bool _lavenderFlag);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets flag for roses
    //----------------------------------------------------------------------------------------------------------------------
    bool rosesFlag() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets flag for roses
    /// @param[in] pass through roses flag
    //----------------------------------------------------------------------------------------------------------------------
    void setRosesFlag(bool _rosesFlag);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets flag for cloves
    //----------------------------------------------------------------------------------------------------------------------
    bool clovesFlag() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets flag for cloves
    /// @param[in] pass through cloves flag
    //----------------------------------------------------------------------------------------------------------------------
    void setClovesFlag(bool _clovesFlag);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets flag for mercury
    //----------------------------------------------------------------------------------------------------------------------
    bool mercuryFlag() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets flag for mercury
    /// @param[in] passes through mercury flag
    //----------------------------------------------------------------------------------------------------------------------
    void setMercuryFlag(bool _mercuryFlag);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets whether the game has won or not
    //----------------------------------------------------------------------------------------------------------------------
    bool gameWon() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets whether the game has won
    /// @param[in] passes through if gameWon variable that sets whether the game has bene won or not
    //----------------------------------------------------------------------------------------------------------------------
    void setGameWon(bool _gameWon);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets whether the game has erupted
    //----------------------------------------------------------------------------------------------------------------------
    bool gameErupted() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets if the game has erupted or not
    /// @param[in] _gameErupted passed through to set whether the volcano has erupted
    //----------------------------------------------------------------------------------------------------------------------
    void setGameErupted(bool _gameErupted);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief gets whether the plague has spread and the timer has ended
    //----------------------------------------------------------------------------------------------------------------------
    bool gameDisease() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief set whether the plague has killed the player
    /// @param[in] passes through if the player died from the plague i.e plague timer ran out
    //----------------------------------------------------------------------------------------------------------------------
    void setGameDisease(bool _gameDisease);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief get if the player has lost the game
    //----------------------------------------------------------------------------------------------------------------------
    bool gameLost() const;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief set if the player has lost the game
    /// @param[in] passes through and sets if the game has been lost
    //----------------------------------------------------------------------------------------------------------------------
    void setGameLost(bool _gameLost);

private:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief cure stage which is the progress of the game
    //----------------------------------------------------------------------------------------------------------------------
    int m_cureStage;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief variable for how many ingredients have been picked up
    //----------------------------------------------------------------------------------------------------------------------
    int m_ingredients;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief variable to store how many hearts are left
    //----------------------------------------------------------------------------------------------------------------------
    int m_heartcount;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag for picking up lavender
    //----------------------------------------------------------------------------------------------------------------------
    bool m_lavenderFlag;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag for picking up roses
    //----------------------------------------------------------------------------------------------------------------------
    bool m_rosesFlag;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag for picking up cloves
    //----------------------------------------------------------------------------------------------------------------------
    bool m_clovesFlag;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag for picking up mercury
    //----------------------------------------------------------------------------------------------------------------------
    bool m_mercuryFlag;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag to determine if the game has been won
    //----------------------------------------------------------------------------------------------------------------------
    bool m_gameWon;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag to determine if the volcano has ended by eruption
    //----------------------------------------------------------------------------------------------------------------------
    bool m_gameErupted;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag to determine if timer ended for disease
    //----------------------------------------------------------------------------------------------------------------------
    bool m_gameDisease;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief flag to set if you lost the game
    //----------------------------------------------------------------------------------------------------------------------
    bool m_gameLost;

};

#endif // GAMEPLAY_H
